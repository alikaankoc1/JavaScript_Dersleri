let cars = ["BMW","MERCEDES","VOLVO","HYUNDAİ","TESLA","TOGG"];

//push method dizinin sonuna eleman ekler.
cars.push("RENAULT");
console.log(cars);

//unshift dizinin başına eleman ekler.
cars.unshift("CHERRY");
console.log(cars);

//pop dizinin son elemanı kaldırır.
cars.pop();
console.log(cars);

//shift dizinin ilk elemanı kaldırır.
cars.shift();
console.log(cars);