// string veri türü: metinsel veriler için kullanılır. Çift tırnak, tek tırnak veya backtick ile tanımlanabilir.

let name = "Ali Kaan Koç";
console.log(typeof name);

// number veri türü: sayısal veriler için kullanılır. Tamsayı ve ondalıklı sayılar için kullanılır.

let age = 20;
console.log(typeof age);
let price = 19.99;
console.log(typeof price);

// boolean veri türü: true ve false olmak üzere iki değerden oluşur.

let a = 18;
let b= 7;
let result = a + b;

console.log(result <52);
console.log(result > 25);

// object veri türü: birden fazla veriyi tek bir değişkende saklamak için kullanılır. İçerisinde anahtar-değer çiftleri bulunur.

let users = {
    name:"Ali Kaan Koç",
    surname: "Koç",
    age: 20,
    job: "Software Developer"
}
console.log(users);
console.log(typeof users);

